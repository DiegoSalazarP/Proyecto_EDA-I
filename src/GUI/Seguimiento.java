    private Expediente buscarExpedientePorCodigo(String codigo) {
        NodoSimple<Expediente> actual = listaExpedientes.getPrimero();
        while (actual != null) {
            if (actual.getDato().getCodigo().equals(codigo)) {
                return actual.getDato();
            }
            actual = actual.getSiguiente();
        }
        return null;
    }
    
