    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         String codigo = txtCodigo.getText();
            Expediente exp = buscarExpedientePorCodigo(codigo);

            if (exp != null) {
            lblEstado.setText(exp.getEstado());

            if (exp.getFechaFin() != null) {
                lblFechaFin.setText(exp.getFechaFin().toString());
            } else {
                lblFechaFin.setText("Aún en trámite");
            }
            } else {
            JOptionPane.showMessageDialog(this, "Expediente no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        String codigo = txtCodigo.getText();
        Expediente exp = buscarExpedientePorCodigo(codigo);

        if(exp != null){
            exp.setFechaFin(LocalDateTime.now());
            exp.setEstado("Finalizado");

            // MOSTRAR EN INTERFAZ
            lblEstado.setText("Finalizado");
            lblFechaFin.setText(exp.getFechaFin().toString());

            JOptionPane.showMessageDialog(this, "Trámite finalizado con éxito.");
        } else {
            JOptionPane.showMessageDialog(this, "Expediente no encontrado.");
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

